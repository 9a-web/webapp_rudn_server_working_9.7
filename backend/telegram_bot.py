WEB_APP_URL = "https://rudn-schedule.ru"

# ID –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–æ–≤ (–º–æ–≥—É—Ç –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –∫–æ–º–∞–Ω–¥—É /users)
ADMIN_IDS = [765963392]

# –ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ MongoDB
